def verify() -> tuple[int, list[str]]:
    checks: list[tuple[bool, str]] = []
    for mod, expected in EXPECTED_PERIODS.items():
        checks.append((phi_order(mod) == expected, f"phi period modulo {mod}"))

    inv = phi_inv(3, 5)
    checks.extend((
        (phi_mul(3, 5) == (5, 8), "PSCALE vector"),
        (phi_conj(3, 5) == (8, 516), "PCHIRAL vector"),
        (phi_mul_full(3, 5, 2, 7) == (41, 66), "PMUL vector"),
        (inv == (513, 5), "PINV vector"),
        (phi_mul_full(3, 5, inv[0], inv[1]) == (1, 0), "inverse identity"),
    ))
    coherent = phi_phslk((3, 5), (2, 7), (6, 10), (4, 14))
    mismatch = phi_phslk((3, 5), (2, 7), (6, 11), (4, 14))
    singular = phi_phslk((3, 5), (1, 100), (6, 10), (4, 14))
    checks.extend((
        (coherent[:2] == (True, False), "PHSLK coherent vector"),
        (mismatch[:2] == (False, False), "PHSLK mismatch vector"),
        (singular[:2] == (False, True), "PHSLK singular vector"),
    ))

    profile = pinv_latency_profile(DEFAULT_MODULUS)
    expected_profile = {
        "nonzero_elements": 270400,
        "zero_divisors": 1041,
        "unique_norms": 520,
        "min_cycles": 3,
        "max_cycles": 23,
    }
    for key, expected in expected_profile.items():
        checks.append((profile[key] == expected, f"PINV profile {key}"))
    checks.append((round(profile["avg_cycles"], 2) == 16.66,
                   "PINV profile average"))

    for relative, marker in EVIDENCE_MARKERS:
        checks.append((marker in (ROOT / relative).read_text(),
                       f"evidence marker {marker}"))

    committed = ROOT / "docs" / "lucas_paper_tables.tex"
    if committed.exists():
        checks.append((committed.read_text() == generated_tables(),
                       "committed generated table is current"))

    failures = [label for passed, label in checks if not passed]
    return len(checks), failures


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--output", type=Path, help="write generated LaTeX")
    parser.add_argument("--verify", action="store_true", help="verify oracle and ledger claims")
    args = parser.parse_args()

    if args.output:
        args.output.write_text(generated_tables())
        print(f"Wrote generated LaTeX to {args.output}")
    elif not args.verify:
        print(generated_tables(), end="")

    if args.verify:
        total, failures = verify()
        passed = total - len(failures)
        print(f"LUCAS publication oracle: {passed}/{total} passed")
        for failure in failures:
            print(f"  FAIL: {failure}")
        return 1 if failures else 0
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
