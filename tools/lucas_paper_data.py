"""Generate and verify the evidence tables for LUCAS_MAC_PAPER.tex.

The arithmetic rows are computed from the same independent Python oracle used
to make the RTL golden vectors.  Physical rows are deliberately artifact-level
measurements copied from the hardware evidence ledger; verification checks the
ledger markers before accepting them.

Usage:
    python3 tools/lucas_paper_data.py --output docs/lucas_paper_tables.tex
    python3 tools/lucas_paper_data.py --verify
"""
from __future__ import annotations

import argparse
import sys
from pathlib import Path


ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(ROOT / "software" / "tests"))

from test_lucas_mac_oracle import (  # noqa: E402
    DEFAULT_MODULUS,
    LUCAS_PRIMES,
    phi_conj,
    phi_inv,
    phi_mul,
    phi_mul_full,
    phi_order,
    phi_phslk,
    pinv_latency_profile,
)


EXPECTED_PERIODS = {11: 10, 29: 14, 199: 22, 521: 26, 3571: 34, 9349: 38}

RESOURCE_ROWS = (
    ("Tang fast probe", "696 LUT4; 216 DFF", "0 / 0", "126.50 MHz; silicon"),
    ("Tang PHSLK probe", "293 LUT4; 146 DFF", "0 / 0", "200.40 MHz; silicon"),
    ("Artix Lucas MAC leaf", "955 estimated LCs", "92 / --", "mapped in LUCAS spin"),
    ("Whole Artix LUCAS spin", "5,073 estimated LCs", "120 / --", "4.41 MHz; silicon"),
)

EVIDENCE_MARKERS = (
    ("docs/hardware_evidence.md", "696 LUT4, 216 DFF, 416 ALU, 0 BRAM, 0 DSP"),
    ("docs/hardware_evidence.md", "126.50 MHz"),
    ("docs/hardware_evidence.md", "293 LUT4, 146 DFF, 182 ALU, 0 BRAM, 0 DSP"),
    ("docs/hardware_evidence.md", "200.40 MHz"),
    ("docs/CURRENT_STATUS.md", "955 estimated LCs"),
    ("docs/CURRENT_STATUS.md", "estimated 5,073 LCs"),
    ("docs/CURRENT_STATUS.md", "120 DSP48E1"),
    ("docs/CURRENT_STATUS.md", "4.41 MHz"),
)


def generated_tables() -> str:
    periods = "\n".join(
        f"{p} & {mod} & {EXPECTED_PERIODS[mod]} \\\\"
        for p, mod in LUCAS_PRIMES.items()
    )
    inv = phi_inv(3, 5)
    ops = (
        (r"$\phi(3+5\phi)$", phi_mul(3, 5), (5, 8)),
        (r"$\overline{3+5\phi}$", phi_conj(3, 5), (8, 516)),
        (r"$(3+5\phi)(2+7\phi)$", phi_mul_full(3, 5, 2, 7), (41, 66)),
        (r"$(3+5\phi)^{-1}$", inv, (513, 5)),
        (r"$(3+5\phi)(3+5\phi)^{-1}$",
         phi_mul_full(3, 5, inv[0], inv[1]), (1, 0)),
    )
    op_rows = "\n".join(
        f"{label} & $({got[0]},{got[1]})$ & "
        f"{'$\\checkmark$' if got == expected else '$\\times$'} \\\\"
        for label, got, expected in ops
    )
    resources = "\n".join(
        f"{name} & {logic} & {dsp_bram} & {evidence} \\\\"
        for name, logic, dsp_bram, evidence in RESOURCE_ROWS
    )
    profile = pinv_latency_profile(DEFAULT_MODULUS)
